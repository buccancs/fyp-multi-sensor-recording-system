    /**
     * Validate MAC address format
     */
    private fun isValidMacAddress(macAddress: String): Boolean {
        val macPattern = "^([0-9A-Fa-f]{2}[:-]){5}([0-9A-Fa-f]{2})$"
        return macAddress.matches(macPattern.toRegex())
    }

